# تابع برای پردازش و یافتن ایموجی با استفاده از الگوی `re`
def find_emoji_response(text):
    for emotion, data in emoji_responses.items():
        emojis = data["emojis"]
        if emotion == "پرچم‌ها":
            for flag, response_list in data["responses"].items():
                if flag in text:
                    return random.choice(response_list)
        else:
            pattern = "|".join(re.escape(emoji) for emoji in emojis)  # ایجاد الگوی `re` برای تطبیق ایموجی‌ها
            if re.search(pattern, text):  # جستجوی الگو در متن
                return random.choice(data["responses"])
    return None  # اگر هیچ ایموجی پیدا نشد، None برگردان






