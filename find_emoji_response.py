# ارسال لیست در چند بخش
async def send_in_chunks(update, text, chunk_size=10):
    # تقسیم متن به چند بخش
    chunks = [text[i:i + chunk_size] for i in range(0, len(text), chunk_size)]
    for chunk in chunks:
        await update.reply(chunk)
        
        


